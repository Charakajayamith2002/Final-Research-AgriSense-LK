// ─── Core engine ──────────────────────────────────────────────────────────────

function agriApplyLanguage(lang) {
  if (!AGRI_TRANSLATIONS[lang]) return;
  const t = AGRI_TRANSLATIONS[lang];

  // Swap text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      const iconEl = el.querySelector('i');
      if (iconEl) {
        Array.from(el.childNodes).forEach(node => {
          if (node.nodeType === Node.TEXT_NODE) node.remove();
        });
        el.appendChild(document.createTextNode(' ' + t[key]));
      } else {
        el.textContent = t[key];
      }
    }
  });

  // Swap placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Update html lang attribute for screen readers / fonts
  document.documentElement.lang = lang === 'si' ? 'si' : lang === 'ta' ? 'ta' : 'en';

  // Highlight active button
  document.querySelectorAll('.agri-lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('agri_lang', lang);
}

function agriInitLanguage() {
  const saved = localStorage.getItem('agri_lang') || 'en';
  agriApplyLanguage(saved);
}

// Run after DOM is ready
document.addEventListener('DOMContentLoaded', agriInitLanguage);
